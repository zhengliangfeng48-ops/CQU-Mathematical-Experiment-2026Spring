figure('Color','white','Position',[100,100,1000,800]);

subplot(2,2,1); 
x = linspace(-4, 6, 1000); 
y1 = exp(-x.^2);
y2 = exp(-(x-2).^2);
y3 = exp(-x.^2 / 4);
y4 = exp(-(x-2).^2 / 4);
plot(x, y1, 'r-', 'LineWidth', 1.2, 'DisplayName', 'y1=e^{-x^2}');
hold on;
plot(x, y2, 'g-', 'LineWidth', 1.2, 'DisplayName', 'y2=e^{-(x-2)^2}');
plot(x, y3, 'b-', 'LineWidth', 1.2, 'DisplayName', 'y3=e^{-x^2/4}');
plot(x, y4, 'm-', 'LineWidth', 1.2, 'DisplayName', 'y4=e^{-(x-2)^2/4}');
hold off;
title('概率曲线');
xlabel('x');
ylabel('y');
legend('Location','best');
grid on;

subplot(2,2,2); 
theta = linspace(0, 2*pi, 1000); 
rho1 = sin(3*theta); 
rho2 = cos(3*theta); 
rho3 = sin(4*theta); 
rho4 = cos(4*theta); 
polarplot(theta, rho1, 'r-', 'LineWidth', 1.2, 'DisplayName', 'ρ=sin3θ');
hold on;
polarplot(theta, rho2, 'g-', 'LineWidth', 1.2, 'DisplayName', 'ρ=cos3θ');
polarplot(theta, rho3, 'b-', 'LineWidth', 1.2, 'DisplayName', 'ρ=sin4θ');
polarplot(theta, rho4, 'm-', 'LineWidth', 1.2, 'DisplayName', 'ρ=cos4θ');
hold off;
title('极坐标线');
legend('Location','best');

subplot(2,2,3); 
t = linspace(0, 2*pi, 1000); 
x = cos(t) .* cos(2*t);
y = sin(t) .* cos(2*t);
plot(x, y, 'k-', 'LineWidth', 1.5);
title('叶形线');
xlabel('x');
ylabel('y');
grid on;
axis equal; 

subplot(2,2,4); 
theta = linspace(0, 2*pi, 1000);
x = -2 + 2*cos(theta);
y = 6 + 3*sin(theta);
plot(x, y, 'b-', 'LineWidth', 1.5);
hold on;
scatter(-2, 6, 80, 'r', 'filled', 'DisplayName', '椭圆中心(-2,6)'); 
hold off;
title('椭圆');
xlabel('x');
ylabel('y');
grid on;
axis equal; 
legend('Location','best');
